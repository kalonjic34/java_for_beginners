package application;

public class App {
    /*
     * More on while loops
     */
    public static void main(String[] args) {
        int count = 0;
        while (count < 10) {
            System.out.printf("Count is: %d\n", count);
            count++;
        }

        int count1 = 7;
        while (count1 > 2) {
            System.out.printf("Count is %d\n", count1);
            count1--;
        }
    }
}