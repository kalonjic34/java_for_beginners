package application;

public class App {
    public static void main(String[] args) {
        // Plus +
        // Divide /
        // Multiply *
        // Substract -

        System.out.println(7 + 3);
        System.out.println(9 / 3);

        System.out.println(10 / 3);

        System.out.println(10.0 / 3.0);
        System.out.println(9 / 4.0);

        System.out.println(3 * 4);
        System.out.println(3 - 4);

    }
}