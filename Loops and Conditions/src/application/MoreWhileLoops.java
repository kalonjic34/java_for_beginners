package application;

public class MoreWhileLoops {
    public static void main(String[] args) {

        int count = 10;

        while (count > 0) {
            System.out.printf("Count is %d\n", count);
            count -= 1;

        }

        int count1 = 7;

        while (count1 > 2) {
            System.out.printf("Count: %d\n", count);
            count1 -= 1;
        }
    }
}